import 'package:qr/qr.dart';

void main() {
  final String dataToEncode = "Hello, QR Code in Console!";
  generateAndDisplayQrCode(dataToEncode);
}

void generateAndDisplayQrCode(String data) {
  final qrCode = QrCode(4, QrErrorCorrectLevel.L) // Version 4, Low error correction
    ..addData(data); // Add the data to encode

  final qrImage = QrImage(qrCode);

  // Print the QR code to the console
  for (var y = 0; y < qrImage.moduleCount; y++) {
    String row = '';
    for (var x = 0; x < qrImage.moduleCount; x++) {
      if (qrImage.isDark(y, x)) {
        row += '██'; // Dark module
      } else {
        row += '  '; // Light module
      }
    }
    print(row);
  }
}
